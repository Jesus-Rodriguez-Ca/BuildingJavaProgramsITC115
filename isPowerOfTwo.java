/*Write a static method isPowerOfTwo that takes an integer n as argument
 * and that returns true if a power of two, and otherwise false. If n is zero or negative, return false. 
 * Note that isPowerOfTwo(1) should return true, since 2^0=1.
 */
public class isPowerOfTwo {

	public static void main(String[] args) {
		isPowerofTwo(0);

	}
	public static void isPowerofTwo(int n) {
		
	
	 if( n % 2 == 0 && n<=1) {
		 System.out.print("True");
	 
	
	}else {
		System.out.print("False");
	
	
		
	}
}
}